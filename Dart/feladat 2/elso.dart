void main() {
  int intValue = 42;
  double doubleValue = 3.14;
  String stringValue = "Hello, Dart!";
  bool boolValue = true;

  int intResult = intValue - 2;
  double doubleResult = doubleValue * 2;

  print("Integer value: $intValue");
  print("Double value: $doubleValue");
  print("String value: $stringValue");
  print("Bool value: $boolValue");
  print("Integer value after -2: $intResult");
  print("Double value after * 2: $doubleResult");

  //bool boolResult != boolValue;
}
